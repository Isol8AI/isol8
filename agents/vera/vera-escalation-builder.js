#!/usr/bin/env node
/**
 * vera-escalation-builder.js
 * Requirements 29, 42, 59: Full context handoff for human agents.
 *
 * Assembles: transcript, customer history, classification, steps attempted,
 * confidence scores, sentiment, CSAT history, and previous ticket outcomes.
 * Deterministic assembly. Zero LLM (suggested resolution added by llm-task in pipeline).
 */

function buildEscalationPackage(data) {
  const {
    conversation = [],
    customer = {},
    classification = {},
    attempts = [],
    confidenceScores = [],
    sentiment = {},
    csatHistory = [],
    previousTickets = [],
    escalationTrigger = 'unknown'
  } = data;

  // Conversation summary stats
  const exchangeCount = conversation.filter(m => m.role === 'customer').length;
  const veraAttempts = attempts.length;

  // CSAT history summary (Req 42)
  const avgCsat = csatHistory.length > 0
    ? Math.round((csatHistory.reduce((sum, c) => sum + c.score, 0) / csatHistory.length) * 10) / 10
    : null;
  const lastCsat = csatHistory.length > 0
    ? csatHistory[csatHistory.length - 1]
    : null;
  const isRepeatIssue = previousTickets.some(t =>
    t.issue_type === classification.subcategory && t.resolved_within_days <= 7
  );

  // Build the package
  const escalationPackage = {
    // Ticket metadata
    ticket_id: data.ticket_id || `ESC-${Date.now()}`,
    escalation_trigger: escalationTrigger,
    escalation_triggers_map: {
      confidence: 'Below 85% confidence — knowledge base may not cover this topic',
      sentiment: 'Customer sentiment shifted to frustrated/angry/distressed',
      request: 'Customer explicitly asked for a human',
      policy_exception: 'Request requires judgment beyond documented policy',
      legal_concern: 'Customer raised legally sensitive topic',
      high_value: 'Transaction exceeds autonomous action threshold',
      exchange_limit: 'Two exchanges without resolution'
    },
    trigger_explanation: null, // set below

    // Customer context (Req 42)
    customer: {
      name: customer.name || null,
      email: customer.email || null,
      account_id: customer.account_id || null,
      customer_since: customer.created_at || null,
      lifetime_value: customer.ltv || null,
      previous_ticket_count: previousTickets.length,
      is_repeat_issue: isRepeatIssue,
      csat_history: {
        average: avgCsat,
        last_score: lastCsat?.score || null,
        last_date: lastCsat?.date || null,
        total_ratings: csatHistory.length
      }
    },

    // Issue context
    issue: {
      classification: classification.category || 'unknown',
      subcategory: classification.subcategory || null,
      compliance_flag: classification.compliance_flag || false,
      channel: data.channel || 'unknown'
    },

    // Conversation context (Req 29)
    conversation: {
      exchange_count: exchangeCount,
      transcript: conversation,
      // Summary generated by summarize CLI in the pipeline
      summary: null
    },

    // What Vera tried (Req 29)
    resolution_attempts: attempts.map(a => ({
      step: a.step,
      action: a.action,
      kb_source: a.kb_source || null,
      confidence: a.confidence || null,
      outcome: a.outcome,
      timestamp: a.timestamp
    })),

    // Confidence history (Req 29)
    confidence: {
      scores: confidenceScores,
      lowest: confidenceScores.length > 0 ? Math.min(...confidenceScores.map(c => c.score)) : null,
      final: confidenceScores.length > 0 ? confidenceScores[confidenceScores.length - 1].score : null
    },

    // Sentiment state (Req 29)
    sentiment: {
      current: sentiment.sentiment || 'unknown',
      score: sentiment.score || 0,
      shift: sentiment.shift || null,
      keywords_detected: sentiment.matched_keywords || []
    },

    // Suggested resolution — populated by llm-task in pipeline
    suggested_resolution: null,

    // Metadata
    escalated_at: new Date().toISOString(),
    priority: getPriority(escalationTrigger, sentiment, classification)
  };

  // Set trigger explanation
  escalationPackage.trigger_explanation =
    escalationPackage.escalation_triggers_map[escalationTrigger] || 'Unknown trigger';

  return escalationPackage;
}

function getPriority(trigger, sentiment, classification) {
  if (trigger === 'legal_concern' || classification.compliance_flag) return 'urgent';
  if (sentiment.sentiment === 'distressed') return 'urgent';
  if (sentiment.sentiment === 'angry') return 'high';
  if (trigger === 'request') return 'high';
  if (trigger === 'high_value') return 'high';
  return 'normal';
}

async function main() {
  const input = await readStdin();
  if (!input) {
    process.stderr.write('No input provided');
    process.exit(1);
  }

  const escalationPackage = buildEscalationPackage(input);

  process.stdout.write(JSON.stringify(escalationPackage));
}

function readStdin() {
  return new Promise((resolve) => {
    let data = '';
    process.stdin.setEncoding('utf8');
    process.stdin.on('data', chunk => data += chunk);
    process.stdin.on('end', () => {
      try { resolve(JSON.parse(data)); }
      catch { resolve(null); }
    });
    if (process.stdin.isTTY) resolve(null);
  });
}

module.exports = { buildEscalationPackage, getPriority };

if (require.main === module) {
  main().catch(err => {
    process.stderr.write(err.message);
    process.exit(1);
  });
}
